    private void searchCustomers(String id) {
        searchCustomer = CustomerController.searchCustomer(idText.getText());
        if (searchCustomer != null) {
            nameText.setText(searchCustomer.getCustname());
            addressText.setText(searchCustomer.getAddress());
            cityText.setText(searchCustomer.getCity());
            mobileText.setText(searchCustomer.getMobile());
            landText.setText(searchCustomer.getLandline());
            storenameText.setText(searchCustomer.getShopName());
        } else {
            nameText.setText(null);
            addressText.setText(null);
            cityText.setText(null);
            mobileText.setText(null);
            landText.setText(null);
            storenameText.setText(null);
            JOptionPane.showMessageDialog(this, "No such customer record", "Alert", 2);
        }
    }
